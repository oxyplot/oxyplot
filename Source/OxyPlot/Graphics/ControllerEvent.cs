namespace OxyPlot
{
    /// <summary>
    /// A list of controller events.
    /// </summary>
    public enum ControllerEvent
    {
        /// <summary>
        /// Gesture event.
        /// </summary>
        Gesture,

        /// <summary>
        /// Mouse down event.
        /// </summary>
        MouseDown,

        /// <summary>
        /// Mouse enter event.
        /// </summary>
        MouseEnter,

        /// <summary>
        /// Mouse leave event.
        /// </summary>
        MouseLeave,

        /// <summary>
        /// Mouse move event.
        /// </summary>
        MouseMove,

        /// <summary>
        /// Mouse up event.
        /// </summary>
        MouseUp,

        /// <summary>
        /// Mouse wheel event.
        /// </summary>
        MouseWheel,

        /// <summary>
        /// Touch started event.
        /// </summary>
        TouchStarted,

        /// <summary>
        /// Touch delta event.
        /// </summary>
        TouchDelta,

        /// <summary>
        /// Touch completed event.
        /// </summary>
        TouchCompleted,

        /// <summary>
        /// Key down event.
        /// </summary>
        KeyDown
    }
}